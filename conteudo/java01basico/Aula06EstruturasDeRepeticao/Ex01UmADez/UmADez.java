package projeto201.conteudo.java01basico.Aula06EstruturasDeRepeticao.Ex01UmADez;
// A estrutura for repete um bloco de código um número determinado de vezes, geralmente composta por três partes:
// inicialização, condição e atualização.
public class UmADez {
    public static void main(String[] args) {
        System.out.println("Contando de 1 a 10");
        for (int num = 0; num < 10 ; num++) {
            System.out.println(num + 1);
        }
    }
}
